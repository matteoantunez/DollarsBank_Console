package com.dollarsbank.utility;

public class ColorsUtility {
	// Colors for the console
	public static final String ANSI_RESET = "\u001B[0m";
	public static final String ANSI_RED = "\u001B[31m";
	public static final String ANSI_GREEN = "\u001B[32m";
	public static final String ANSI_YELLOW = "\u001B[33m";
	public static final String ANSI_BLUE = "\u001B[34m";
	public static final String ANSI_PRPLE = "\u001B[35m";
	public static final String ANSI_CYAN = "\u001B[36m";
	public static final String ANSI_WHITE = "\u001B[37m";
	
	public String getAnsiReset() {
		return ANSI_RESET;
	}
	public String getAnsiRed() {
		return ANSI_RED;
	}
	public String getAnsiGreen() {
		return ANSI_GREEN;
	}
	public String getAnsiYellow() {
		return ANSI_YELLOW;
	}
	public String getAnsiBlue() {
		return ANSI_BLUE;
	}
	public String getAnsiPrple() {
		return ANSI_PRPLE;
	}
	public String getAnsiCyan() {
		return ANSI_CYAN;
	}
	public String getAnsiWhite() {
		return ANSI_WHITE;
	}
	
}
